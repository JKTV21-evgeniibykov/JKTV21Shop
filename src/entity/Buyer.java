package entity;

/**
 *
 * @author pupil
 */
public class Buyer {
    
    private String name;
    private String phone;

    public Buyer() {
    }

    public Buyer(String name, String phone) {
        this.name = name;
        this.phone = phone;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getName() {
        return name;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }

    public String getPhone() {
        return phone;
    }

    @Override
    public String toString() {
        return "buyer{" + 
                "name=" + name + 
                ", phone=" + phone + 
                '}';
    }
    
}
